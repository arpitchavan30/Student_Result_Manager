    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String roll=jTextField1.getText();
        String math=jTextField2.getText();
        String som=jTextField3.getText();
        String em=jTextField4.getText();
        String tom=jTextField5.getText();
        String ms=jTextField6.getText();
        
        try{
            // connection to database
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/arc","root","Arpya@3713");
            
            Statement st=con.createStatement();
            // first check given roll no student is presnt in database or not
            ResultSet rs=st.executeQuery("select * from student where rollno='"+roll+"'");
            
            if(rs.next()){
                // if present then update result
                st.executeUpdate("insert into result(rollno,math,som,em,tom,ms) values('"+roll+"','"+math+"','"+som+"','"+em+"','"+tom+"','"+ms+"')");
                JOptionPane.showMessageDialog(null,"Marks updated succesfully");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"This Roll No is not registered");
            }
            
            
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

